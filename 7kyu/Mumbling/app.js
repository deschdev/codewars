const addHyphen = (str) => {
  return str.split("").map((char, i) => (char.toUpperCase() + char.toLowerCase().repeat(i))).join("-")
}

console.log(addHyphen("ZpglnRxqenU"), "Z-Pp-Ggg-Llll-Nnnnn-Rrrrrr-Xxxxxxx-Qqqqqqqq-Eeeeeeeee-Nnnnnnnnnn-Uuuuuuuuuuu");
